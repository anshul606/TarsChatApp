export function EmojiPickerInput({
  onEmojiSelect,
  className,
}: EmojiPickerInputProps) {
  const [isOpen, setIsOpen] = useState(false);
  const [activeCategory, setActiveCategory] = useState(
    Object.keys(EMOJI_CATEGORIES)[0],
  );
  const pickerRef = useRef<HTMLDivElement>(null);

  useEffect(() => {
    const handleClickOutside = (event: MouseEvent) => {
      if (
        pickerRef.current &&
        !pickerRef.current.contains(event.target as Node)
      ) {
        setIsOpen(false);
      }
    };

    if (isOpen) {
      document.addEventListener("mousedown", handleClickOutside);
      return () => {
        document.removeEventListener("mousedown", handleClickOutside);
      };
    }
  }, [isOpen]);

  const handleEmojiClick = (emoji: string) => {
    onEmojiSelect(emoji);
  };

  return (
    <div className={cn("relative", className)} ref={pickerRef}>
      <Button
        type="button"
        variant="ghost"
        size="icon"
        onClick={() => setIsOpen(!isOpen)}
        aria-label="Open emoji picker"
        title="Add emoji"
      >
        <Smile className="h-5 w-5" />
      </Button>

      {isOpen && (
        <div className="absolute bottom-full left-0 mb-2 w-[420px] bg-background border rounded-lg shadow-xl animate-in fade-in-0 zoom-in-95">
          {/* Category Tabs */}
          <div className="flex gap-1 p-2 border-b overflow-x-auto scrollbar-thin">
            {Object.keys(EMOJI_CATEGORIES).map((category) => (
              <button
                key={category}
                type="button"
                onClick={() => setActiveCategory(category)}
                className={cn(
                  "px-3 py-2 text-sm rounded-md transition-colors whitespace-nowrap shrink-0",
                  activeCategory === category
                    ? "bg-primary text-primary-foreground"
                    : "hover:bg-accent",
                )}
              >
                {category.split(" ")[0]}
              </button>
            ))}
          </div>

          {/* Emoji Grid */}
          <ScrollArea className="h-80">
            <div className="p-4">
              <h3 className="text-sm font-medium mb-3 text-muted-foreground">
                {activeCategory}
              </h3>
              <div className="grid grid-cols-9 gap-1">
                {EMOJI_CATEGORIES[
                  activeCategory as keyof typeof EMOJI_CATEGORIES
                ].map((emoji, index) => (
                  <button
                    key={`${emoji}-${index}`}
                    type="button"
                    onClick={() => handleEmojiClick(emoji)}
                    className="h-10 w-10 flex items-center justify-center text-2xl hover:bg-accent rounded-md transition-colors"
                    aria-label={`Insert ${emoji}`}
                  >
                    {emoji}
                  </button>
                ))}
              </div>
            </div>
          </ScrollArea>
        </div>
      )}
    </div>
  );
}
